package org.bukkit;

/**
 * 树的种类
 */
public enum TreeType {

    /**
     * 普通的树
     */
    TREE,
    /**
     * 大树
     */
    BIG_TREE,
    /**
     * 普通云杉树
     */
    REDWOOD,
    /**
     * 瘦长的云杉树，顶端只有几个叶子
     */
    TALL_REDWOOD,
    /**
     * 桦树
     */
    BIRCH,
    /**
     * 大丛林树(树干2x2)
     */
    JUNGLE,
    /**
     * 普通丛林树
     */
    SMALL_JUNGLE,
    /**
     * 有可可在上面的丛林树
     */
    COCOA_TREE,
    /**
     * 丛林的小灌木从
     */
    JUNGLE_BUSH,
    /**
     * 大红蘑菇
     */
    RED_MUSHROOM,
    /**
     * 大棕色蘑菇
     */
    BROWN_MUSHROOM,
    /**
     * 沼泽树(旁边有藤蔓)
     */
    SWAMP,
    /**
     * 金合欢树.
     */
    ACACIA,
    /**
     * 深色橡木树.
     */
    DARK_OAK,
    /**
     * 大云杉树(树干2x2)
     */
    MEGA_REDWOOD,
    /**
     * 大桦树
     */
    TALL_BIRCH,
}
