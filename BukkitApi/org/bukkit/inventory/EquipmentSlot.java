package org.bukkit.inventory;

/**
 * 一个描述玩家身体部位的枚举
 */
public enum EquipmentSlot {

    /**
     * 玩家主手
     */
    HAND,
    /**
     * 玩家副手
     */
    OFF_HAND,
    /**
     * 头部位置
     */
    FEET,
    /**
     * 胸部位置
     */
    LEGS,
    /**
     * 腿部位置
     */
    CHEST,
    /**
     * 脚部位置
     */
    HEAD
}
