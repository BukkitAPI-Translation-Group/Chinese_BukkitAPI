package org.bukkit;

/**
 * 表示三维笛卡尔坐标系中相互垂直的轴. 在Minecraft中, x、z轴位于水平面上, 而y轴指向上. 
 * <p>
 * 原文：
 * Represents a mutually perpendicular axis in 3D Cartesian coordinates. In
 * Minecraft the x, z axes lie in the horizontal plane, whilst the y axis points
 * upwards.
 */
public enum Axis {
    /**
     * X轴
     */
    X,
    /**
     * Y轴
     */
    Y,
    /**
     * Z轴
     */
    Z;
}
